	<h1>Mon histoire</h1>
	<br />
	<p>
		Les taupes et leur piégeage, c’est une longue histoire entre elles et
		moi qui remonte à loin.
	</p>

	<p>
		Dans les années quatre-vingt, pendant mes études agricoles, lors de
		nombreux stages dans les fermes, j’ai découvert le piégeage de taupes
		dans une ferme située dans les plaines d’anciennes tourbières à Mesplède
		(64370).
	</p>

	<p>
		Dans cette ferme de vaches laitières vivait un grand-père de plus de
		quatre-vingt-dix ans qui avait comme principales occupations à la ferme
		de couper du bois en petits morceaux pour alimenter la cuisinière à
		bois, chasser les rats au fusil et surtout piéger les taupes à
		l’ancienne avec les pièges mécanique putange. C’est ce vieil homme qui
		m’a initié, moi le jeune stagiaire d’à peine quatorze ans, au piégeage
		de la taupe en me confiant tous ses secrets en piégeant dans ces terres
		d’anciennes tourbières noires et grasses grouillant de vers et donc de
		taupes qui entravaient l’activité agricole.
	</p>

	<p>
		J’ai alors investi dans l’achat de plusieurs dizaines de pièges avec mes
		petites indemnités de stagiaire. Mon maître de stage proposa de me
		donner 5 Francs par taupe capturée ce qui me faisait un bon agent de
		poche puisque j’en attrapais beaucoup grâce à la formation du vieux
		paysan qui en piégeait depuis sa jeunesse.
	</p>

	<p>
		Pendant plusieurs mois, j’ai été étais sollicité comme jeune taupier de
		quatorze ans par tous les voisins agriculteurs qui apercevaient mes
		piquets avec leurs drapeaux de couleur dans les champs pour signaler et
		retrouver les pièges en place.
	</p>

	<p>
		Très jeune, j’ai toujours été passionné par la terre, la vie du sol avec
		ceux qui y vivent et c’est pourquoi j’ai toujours travaillé dans ce
		milieu en créant et gérant une société de jardinier-paysagiste
		spécialisée dans l’entretien de parcs et jardins ou l’élimination des
		taupes faisait partie de mes prestations.
	</p>

	<p>
		Par la suite, exerçant la profession de maraîcher bio, avec un système
		agro-forestier et en permaculture, je devais également éliminer les
		taupes lorsqu’elles entravaient mon activité.
	</p>

	<p>
		Aujourd’hui mon métier est celui de taupier professionnel et mon
		activité principale est le piégeage mécanique de taupes et rat taupier
		(campagnols) dans tout le sud de la France.
	</p>

	<p>
		J’envisage également de me spécialiser dans le piégeage de divers
		nuisibles dans l’avenir.
	</p>

